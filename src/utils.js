export function arrayMove(arr, previousIndex, newIndex) {
  const array = [...arr];
  if (newIndex >= array.length) {
    let k = newIndex - array.length;
    while (k-- + 1) {
      array.push(undefined);
    }
  }
  [array[previousIndex], array[newIndex]] = [array[newIndex], array[previousIndex]];  // swap
  return array;
}

